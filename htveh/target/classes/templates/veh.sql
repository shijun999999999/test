CREATE TABLE `hengtong`.`t_veh`
( 
	`id` INT NOT NULL AUTO_INCREMENT COMMENT '车辆信息状态表', 
	`sn` VARCHAR(40) NOT NULL COMMENT '本地流水号', 
	`sfsn` VARCHAR(40) NOT NULL COMMENT '安检流水号', 
	`envsn` VARCHAR(40) NOT NULL COMMENT '环检流水号', 
	`plateNumber` VARCHAR(8) NOT NULL COMMENT '号牌号码', 
	`plateType` VARCHAR(2) NOT NULL COMMENT '号牌种类，遵照国标', 
	`vehType` VARCHAR(3) NOT NULL COMMENT '车辆类型，遵照国标', 
	`safetyInspects` VARCHAR(60) COMMENT '安检项目，英文字符\',\'分隔，遵照国标', 
	`safetyStart` TIMESTAMP COMMENT '安检开始时间', 
	`externalInspector` VARCHAR(10) COMMENT '外检员', 
	`externalStart` TIMESTAMP COMMENT '外检开始时间', 
	`externalEnd` TIMESTAMP COMMENT '外检结束时间', 
	`chassisInspector` VARCHAR(10) COMMENT '底盘检验员', 
	`chassisStart` TIMESTAMP COMMENT '底盘检验开始时间', 
	`chassisEnd` TIMESTAMP COMMENT '底盘检验结束时间', 
	`dynamicInspector` VARCHAR(10) COMMENT '底盘动态检验员', 
	`dynamicStart` TIMESTAMP COMMENT '底盘动态开始时间', 
	`dynamicEnd` TIMESTAMP COMMENT '底盘动态结束时间', 
	`guide` VARCHAR(10) COMMENT '引车员', 
	`guideStart` TIMESTAMP COMMENT '线上检验开始时间', 
	`guideEnd` TIMESTAMP COMMENT '线上检验结束时间', 
	`safetyEnd` TIMESTAMP COMMENT '安检结束时间', 
	`frequencySafety` INT DEFAULT 1 COMMENT '安检次数', 
	`conclusionSafety` VARCHAR(4) COMMENT '安检结论，固定值：合格/不合格', 
	`stateSafety` INT DEFAULT 1 COMMENT '车辆安检状态，1已登记，2外观检测中，3外观检测结束，4底盘检测中，5底盘检测结束，6底盘动态检测中，7底盘动态检测结束，8上线检测中，9下线结束，10外廓检测中，11外廓检测结束，12整备检测中，13整备检测结束，14路试中，15路试结束，16安检流程结束', 
	`envInspector` VARCHAR(10) COMMENT '环检员', 
	`envInspects` VARCHAR(60) COMMENT '环检项目，英文字符\',\'分隔', 
	`envStart` TIMESTAMP COMMENT '环检开始时间', 
	`envEnd` TIMESTAMP COMMENT '环检结束时间', 
	`frequencyEnv` INT DEFAULT 1 COMMENT '环检次数', 
	`conclusionEnv` VARCHAR(4) COMMENT '环检结论，固定值：合格/不合格', 
	`stateEnv` INT DEFAULT 1 COMMENT '车辆环检状态，1已登记，2环检检测中，3环检结束', 
	`state` INT DEFAULT 1 COMMENT '车辆状态，1已登记，2全部项目完成/待支付，3已支付/待签证下发，4签证下发/车辆结束', 
	`createUser` INT DEFAULT 1, 
	`createTime` TIMESTAMP DEFAULT CURRENT_TIMESTAMP, 
	`updateUser` INT DEFAULT 1, 
	`updateTime` TIMESTAMP DEFAULT CURRENT_TIMESTAMP, 
	PRIMARY KEY (`id`) 
); 
